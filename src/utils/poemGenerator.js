export function generateAcrosticPoem(name) {
  if (!name || name.trim().length === 0) {
    return ['请输入您的名字', '让我为您生成专属藏头诗'];
  }

  const characters = name.trim().split('').slice(0, 4); // Max 4 characters
  const poem = [];

  characters.forEach((char, index) => {
    if (poemDatabase[char]) {
      poem.push(poemDatabase[char]);
    } else {
      // Use generic line if character not found
      poem.push(`${char}${genericLines[index % genericLines.length].substring(1)}`);
    }
  });

  return poem;
}

